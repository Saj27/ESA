package Model;

/**
 *
 * @author Owner
 */
public class Student {


    private String studentID,email,fName,lName,address,gender, grade,password;
  
 


  public Student(String studentID, String email, String fName, String lName, String address, String gender,String grade, String password ) {
        this.studentID = studentID;
        this.email = email;
        this.fName = fName;
        this.lName = lName;
        this.address = address;
        this.gender = gender;
        this.grade = grade;
        this.password = password;
       ;
    }

    
  
  
    public String getStudentID() {
        return studentID;
    }

    public void setStudentID(String studentID) {
        this.studentID = studentID;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public String getlName() {
        return lName;
    }

    public void setlName(String lName) {
        this.lName = lName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
